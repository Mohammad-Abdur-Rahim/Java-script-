//  Array code here
const number_list = [1,2,3,4,5];
console.log(number_list);
//array push fn
number_list.push(6,7,8);
number_list.push(9,10);
console.log(number_list);
//array pop fn
number_list.pop();
console.log(number_list);

//  Array Shift and unshift
const number = [11,12,13,14];
console.log(number);
number.shift(); //pop first
console.log(number);
number.unshift(10); //push first
console.log(number);


